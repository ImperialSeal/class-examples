package com.ncblazers.colosseum;

public class Mage extends Fighter {
	private String element;
	private int mana;
	
	public String getElement() {
		return element;
	}
	public void setElement(String element) {
		this.element = element;
	}
	public int getMana() {
		return mana;
	}
	public void setMana(int mana) {
		this.mana = mana;
	}
	
	
}
